DROP TABLE IF EXISTS AUTHOR;
CREATE TABLE AUTHOR(ID BIGINT IDENTITY PRIMARY KEY, NAME VARCHAR(255) NOT NULL);

DROP TABLE IF EXISTS GENRE;
CREATE TABLE GENRE(ID BIGINT IDENTITY PRIMARY KEY, GENRE VARCHAR(255) NOT NULL);

 DROP TABLE IF EXISTS BOOK;
 CREATE TABLE BOOK(
 ID BIGINT IDENTITY PRIMARY KEY,
 TITLE VARCHAR(255) NOT NULL,
 AUTHOR_ID BIGINT REFERENCES AUTHOR(ID),
 GENRE_ID BIGINT REFERENCES GENRE(ID),
 CONSTRAINT AUTHOR_ID_FK FOREIGN KEY (AUTHOR_ID) REFERENCES AUTHOR(ID),
 CONSTRAINT GENRE_ID_FL FOREIGN KEY (GENRE_ID) REFERENCES GENRE(ID));

 DROP TABLE IF EXISTS COMMENT;
CREATE TABLE COMMENT(
ID BIGINT IDENTITY PRIMARY KEY,
COMMENT VARCHAR(255) NOT NULL,
OWNER VARCHAR(255) NOT NULL,
BOOK_ID BIGINT REFERENCES BOOK(ID) ON DELETE CASCADE
 );